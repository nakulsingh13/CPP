#include<iostream>
using namespace std;

int main(){
    int a;
    cout<<" // Size of integer "<<sizeof(a)<<endl;
    
    float b;
    cout<<" // Size of float "<<sizeof(b)<<endl;

    char c;
    cout<<" // Size of char "<<sizeof(c)<<endl;

    double d;
    cout<<" // Size of double "<<sizeof(d)<<endl;

    bool e;
    cout<<" // Size of bool "<<sizeof(e)<<endl;


    return 0;
}